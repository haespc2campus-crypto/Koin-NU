create policy "admin semua profiles" on profiles for all using (current_app_role() = 'admin') with check (current_app_role() = 'admin');
create policy "user baca profil sendiri" on profiles for select using (id = auth.uid());

create policy "ranting publik baca" on ranting_profile for select using (true);
create policy "admin kelola ranting" on ranting_profile for all using (current_app_role() = 'admin') with check (current_app_role() = 'admin');

create policy "pengurus publik baca aktif" on pengurus for select using (status = 'aktif');
create policy "admin kelola pengurus" on pengurus for all using (current_app_role() = 'admin') with check (current_app_role() = 'admin');

create policy "admin bendahara baca petugas" on petugas for select using (current_app_role() in ('admin', 'bendahara'));
create policy "admin kelola petugas" on petugas for all using (current_app_role() = 'admin') with check (current_app_role() = 'admin');

create policy "admin bendahara semua donatur" on donatur for select using (current_app_role() in ('admin', 'bendahara'));
create policy "petugas baca donatur wilayah" on donatur for select using (
  current_app_role() = 'petugas'
  and exists (
    select 1 from petugas p
    join profiles pr on pr.phone = p.phone
    where pr.id = auth.uid()
      and (p.id = donatur.petugas_id or (p.rt = donatur.rt and p.rw = donatur.rw))
  )
);
create policy "admin bendahara kelola donatur" on donatur for all using (current_app_role() in ('admin', 'bendahara')) with check (current_app_role() in ('admin', 'bendahara'));

create policy "admin bendahara baca pengambilan" on pengambilan_koin for select using (current_app_role() in ('admin', 'bendahara'));
create policy "petugas baca transaksi milik" on pengambilan_koin for select using (
  current_app_role() = 'petugas'
  and exists (
    select 1 from petugas p
    join profiles pr on pr.phone = p.phone
    where pr.id = auth.uid() and p.id = pengambilan_koin.petugas_id
  )
);
create policy "petugas input transaksi" on pengambilan_koin for insert with check (current_app_role() in ('admin', 'petugas'));
create policy "admin petugas update transaksi" on pengambilan_koin for update using (current_app_role() in ('admin', 'petugas', 'bendahara')) with check (current_app_role() in ('admin', 'petugas', 'bendahara'));

