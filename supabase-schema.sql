create extension if not exists pgcrypto;

create table if not exists profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique,
  full_name text not null,
  role text not null default 'petugas' check (role in ('admin', 'bendahara', 'petugas', 'pengurus')),
  phone text,
  status text not null default 'aktif' check (status in ('aktif', 'tidak_aktif')),
  created_at timestamptz not null default now()
);

create table if not exists ranting_profile (
  id bigint generated by default as identity primary key,
  nama_ranting text not null,
  desa text not null,
  kecamatan text not null,
  kabupaten text not null,
  provinsi text not null,
  alamat_sekretariat text not null,
  email text,
  phone text,
  masa_khidmah text,
  logo_url text,
  created_at timestamptz not null default now()
);

create table if not exists pengurus (
  id bigint generated by default as identity primary key,
  nama text not null,
  jabatan text not null,
  phone text,
  alamat text,
  foto_url text,
  status text not null default 'aktif' check (status in ('aktif', 'tidak_aktif')),
  created_at timestamptz not null default now()
);

create table if not exists petugas (
  id bigint generated by default as identity primary key,
  nama text not null,
  phone text,
  alamat text,
  rt text,
  rw text,
  username text unique,
  wilayah_tugas text,
  status text not null default 'aktif' check (status in ('aktif', 'tidak_aktif')),
  created_at timestamptz not null default now()
);

create table if not exists donatur (
  id bigint generated by default as identity primary key,
  nama_kk text not null,
  alamat text,
  rt text,
  rw text,
  phone text,
  petugas_id bigint references petugas(id) on delete set null,
  kode_donatur text,
  qr_code_value text,
  qr_code_image text,
  status text not null default 'aktif' check (status in ('aktif', 'tidak_aktif')),
  catatan text,
  created_at timestamptz not null default now()
);

create table if not exists pengambilan_koin (
  id bigint generated by default as identity primary key,
  nomor_transaksi text not null unique,
  tanggal date not null,
  donatur_id bigint references donatur(id) on delete set null,
  petugas_id bigint references petugas(id) on delete set null,
  nominal numeric(14,2) not null default 0,
  metode_pembayaran text not null check (metode_pembayaran in ('Tunai', 'Transfer', 'QRIS')),
  catatan_petugas text,
  bukti_foto_path text,
  bukti_foto_url text,
  bukti_foto_nama text,
  status_verifikasi text not null default 'Menunggu Verifikasi' check (status_verifikasi in ('Menunggu Verifikasi', 'Disetujui Bendahara', 'Ditolak', 'Perlu Revisi')),
  status_notifikasi text not null default 'belum_dikirim' check (status_notifikasi in ('belum_dikirim', 'terkirim_wa', 'nomor_kosong', 'cetak_bukti', 'konfirmasi_manual')),
  waktu_notifikasi timestamptz,
  catatan_notifikasi text,
  riwayat_notifikasi jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists verifikasi_setoran (
  id bigint generated by default as identity primary key,
  pengambilan_id bigint not null references pengambilan_koin(id) on delete cascade,
  bendahara_id uuid references profiles(id) on delete set null,
  status text not null check (status in ('Disetujui Bendahara', 'Ditolak', 'Perlu Revisi')),
  catatan_bendahara text,
  bukti_setoran_path text,
  bukti_setoran_url text,
  bukti_setoran_nama text,
  tanggal_verifikasi date not null default current_date,
  created_at timestamptz not null default now()
);

create table if not exists setoran_petugas (
  id bigint generated by default as identity primary key,
  nomor_setoran text not null unique,
  petugas_id bigint references petugas(id) on delete set null,
  tanggal_setor date not null,
  periode_mulai date not null,
  periode_selesai date not null,
  nominal numeric(14,2) not null default 0,
  jumlah_transaksi integer not null default 0,
  metode_setor text not null check (metode_setor in ('Tunai', 'Transfer', 'QRIS')),
  catatan_petugas text,
  bukti_setor_path text,
  bukti_setor_url text,
  bukti_setor_nama text,
  status text not null default 'Menunggu Verifikasi' check (status in ('Menunggu Verifikasi', 'Diterima Bendahara', 'Dikembalikan Revisi', 'Ditolak')),
  created_at timestamptz not null default now()
);

create table if not exists setoran_lazisnu (
  id bigint generated by default as identity primary key,
  nomor_setoran text not null unique,
  tanggal_setor date not null,
  tujuan_setoran text not null check (tujuan_setoran in ('LAZISNU Ranting', 'MWC LAZISNU', 'PC LAZISNU')),
  nama_penerima text not null,
  nominal numeric(14,2) not null default 0,
  metode_setor text not null check (metode_setor in ('Tunai', 'Transfer', 'QRIS')),
  nomor_bukti text,
  bukti_setor_path text,
  bukti_setor_url text,
  bukti_setor_nama text,
  catatan text,
  status text not null default 'Draft' check (status in ('Draft', 'Sudah Disetor', 'Batal')),
  created_at timestamptz not null default now()
);

create table if not exists penyaluran_dana (
  id bigint generated by default as identity primary key,
  nomor_penyaluran text not null unique,
  tanggal date not null,
  nama_penerima text not null,
  alamat text,
  rt text,
  rw text,
  phone text,
  kategori_bantuan text not null,
  nominal numeric(14,2) not null default 0,
  sumber_dana text,
  status text not null default 'Draft' check (status in ('Draft', 'Disalurkan', 'Dibatalkan')),
  keterangan text,
  dokumentasi_path text,
  dokumentasi_url text,
  dokumentasi_nama text,
  created_at timestamptz not null default now()
);

create table if not exists dokumentasi_kegiatan (
  id bigint generated by default as identity primary key,
  judul text not null,
  kategori text not null,
  tanggal date not null,
  foto_path text,
  foto_url text not null,
  foto_nama text,
  created_at timestamptz not null default now()
);

create table if not exists settings (
  id text primary key,
  key text not null unique,
  value jsonb not null default 'null'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

