create or replace view public_transparansi_pengambilan as
select tanggal, nominal, metode_pembayaran, status_verifikasi
from pengambilan_koin
where status_verifikasi = 'Disetujui Bendahara';

create or replace view public_transparansi_penyaluran as
select tanggal, kategori_bantuan, nominal, status
from penyaluran_dana
where status = 'Disalurkan';

